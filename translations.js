// Helper: get merged dict for a page
window.I18N_GET = function(pageId, lang) {
  var all = window.I18N_ALL;
  var common = (all._common && all._common[lang]) || {};
  var tools = (all._tools && all._tools[lang]) || {};
  var page = (all[pageId] && all[pageId][lang]) || {};
  var merged = {};
  for (var k in common) merged[k] = common[k];
  for (var k in tools) merged[k] = tools[k];
  for (var k in page) merged[k] = page[k];
  return merged;
};